#include "libft.h"

int		ft_atoi(const char *str)
{
	size_t	i;
	size_t	number;
	int		sign;

	i = 0;
	sign = 1;
	number = 0;
	while ((str[i] >= 7 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		number = (number * 10 + (str[i] - '0'));
		i++;
	}
	return (number * sign);
}
